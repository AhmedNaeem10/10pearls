CREATE TABLE SERVICE_DETAILS(
	SERVICE_DETAIL_ID INT AUTO_INCREMENT,
    WORKER_ID INT,
    SERVICE_ID INT NOT NULL,
    SERVICE_CHARGES FLOAT NOT NULL,
    
    CHECK(SERVICE_CHARGES > 0),
    
    CONSTRAINT PK_SERVICE_DETAIL PRIMARY KEY (SERVICE_DETAIL_ID),
