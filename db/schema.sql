CREATE TABLE SERVICES(
	SERVICE_ID INT AUTO_INCREMENT,
    SERVICE_TITLE VARCHAR(20) NOT NULL UNIQUE,
    SERVICE_DESCRIPTION VARCHAR(200),
    SERVICE_IMAGE VARCHAR(500),
    SERVICE_RATE FLOAT,
    
    CONSTRAINT PK_SERVICE PRIMARY KEY (SERVICE_ID)
);

CREATE TABLE JOBS(
	JOB_ID INT AUTO_INCREMENT,
    CUSTOMER_ID INT NOT NULL,
    SERVICE_DETAIL_ID INT NOT NULL,
    DATE_TIME TIMESTAMP NOT NULL,
    PAYMENT_METHOD VARCHAR(15),
    JOB_STATUS VARCHAR(15) NOT NULL,
    
    CONSTRAINT PK_SERVICE PRIMARY KEY (JOB_ID),
    CONSTRAINT FK_SERVICE_SERVICE_DETAIL FOREIGN KEY (SERVICE_DETAIL_ID) REFERENCES SERVICE_DETAILS(SERVICE_DETAIL_ID)
);

